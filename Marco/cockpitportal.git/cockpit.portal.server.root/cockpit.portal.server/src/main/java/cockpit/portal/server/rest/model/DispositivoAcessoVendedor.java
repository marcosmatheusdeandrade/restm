package cockpit.portal.server.rest.model;

public class DispositivoAcessoVendedor {

	private String modelo;
	
	private String chaveDispositivo;

	private String codigoAcesso;

	public String getModelo() {
		return modelo;
	}

	public void setModelo(String modelo) {
		this.modelo = modelo;
	}

	public String getChaveDispositivo() {
		return chaveDispositivo;
	}

	public void setChaveDispositivo(String chaveDispositivo) {
		this.chaveDispositivo = chaveDispositivo;
	}

	public String getCodigoAcesso() {
		return codigoAcesso;
	}

	public void setCodigoAcesso(String codigoAcesso) {
		this.codigoAcesso = codigoAcesso;
	}

}
